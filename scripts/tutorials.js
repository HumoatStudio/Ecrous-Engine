//Tutorials

function openTutorials() {
    const TutorialsPanel = document.getElementById('TutorialsPanel');
    TutorialsPanel.style.display = 'flex'; // Показываем панель
}

function closeTutorials() {
    const TutorialsPanel = document.getElementById('TutorialsPanel');
    TutorialsPanel.style.display = 'none'; // Скрываем панель
}